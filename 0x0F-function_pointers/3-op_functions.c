#include "3-calc.h"
/**
*op_add - addition
*@a: 1st integer
*@b: 2nd integer
*Return: addition
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub - substraction
*@a: 1st integer
*@b: 2nd integer
*Return: substraction
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul - multiplication
*@a: 1st integer
*@b: 2nd integer
*Return: multiplication
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div - division
*@a: 1st argument
*@b: 2nd argument
*Return: division
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod- modulus
*@a: 1st integer
*@b: 2nd integer
*Return: remainder
*/
int op_mod(int a, int b)
{
return (a % b);
}
